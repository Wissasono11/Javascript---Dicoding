// deklaratif adalah cara penulisan kode yang lebih fokus pada apa yang harus dilakukan, bukan bagaimana cara melakukannya.

// contoh kode program deklaratif
const names = ['Alice', 'Bob', 'Charlie', 'David'];

// penerapan map dalam deklaratif
const newNamesWithExcMark = names.map(name => `${name}!`);

console.log(newNamesWithExcMark); // ['Alice!', 'Bob!', 'Charlie!', 'David!']